class Solution {
  public:
    long long reversedBits(long long x) {
        string temp=bitset<32>(x).to_string();
        // cout<<temp<<" ";
        reverse(temp.begin(),temp.end());
        // cout<<temp;
        return stol(temp,0,2);
    }
};

