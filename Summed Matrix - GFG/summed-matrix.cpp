class Solution {
  public:
    long long sumMatrix(long long n, long long q) {
        if(q>n+n) return 0;
        long long temp=n+1;
        long long temp2=abs(q-temp);
        long long ans=n-temp2;
        return ans;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        long long n,q;
        
        cin>>n>>q;

        Solution ob;
        cout << ob.sumMatrix(n,q) << endl;
    }
    return 0;
}
